package com.Imooc;

public class FloatDemo {
	public static void main(String[] args) {
		//定义一个float型的变量f，初期値为1234.328
		float f = 1234.328f;
		System.out.println("f = " + f);
		//定义一个double型的变量d，初期値5623.464
		double d = 5623.464;
		System.out.println("d = " + d);
		//定义一个double型的变量d1，初期値为123
		double d1 = 123;
		System.out.println("d1 = " + d1); //出力为浮点型数值
		//定义一个double型的变量d2，使其等于d
		double d2 = d;
		System.out.println("d2 = " + d2);
	}
}
