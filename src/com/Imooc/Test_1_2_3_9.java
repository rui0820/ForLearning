package com.Imooc;

public class Test_1_2_3_9 {
	public static void main(String[] args) {
	//定义一个float型变量f1，初期値为98.4
	float f1 = 98.4f;
	System.out.println("f1 = " + f1);
	//定义一个float型变量f2，使其等于f1
	float f2 = f1;
	System.out.println("f2 = " + f2);
	//定义一个整型变量n,初期値为55
	int n = 55;
	System.out.println("n = " + n);
	//定义一个double型变量d1,初期値为555.3
	double d1 = 555.3d;
	System.out.println("d1 = " + d1);
	//把整型变量n的值赋给double型变量d1
	d1 = n;
	System.out.println("d1 = " + d1);
	}
}
